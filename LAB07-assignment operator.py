#LAB07 Assignment operators (ตัวดำเนินการกำหนดค่า)

x,y=int(input("กรอกค่า x : ")),int(input("กรอกค่า y : "))
#x = x+y
x+=y
print("ผลลัพธ์ =", x)

X,Y=int(input("กรอกค่า X : ")),int(input("กรอกค่า Y : "))
#X = X*Y
X*=Y
print("ผลลัพธ์ =", X)

a,b=int(input("กรอกค่า a : ")),int(input("กรอกค่า b : "))
#a = a**b
a**=b
print("ผลลัพธ์ =", a)
